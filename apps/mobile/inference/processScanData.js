export default function processScanData(scanData) {
    let resArr = [];
    for (let i = 0; i < 10; i++) {
        resArr[i] = [];
        for (let j = 0; j < 19; j++) {
            resArr[i][j] = [];
        }

        for (let j = 0; j < 20; j++) {
            let values = Object.values(scanData[i * 20 + j]);
            for (let l = 0; l < 19; l++) {
                resArr[i][l][j] = (values[l] - mean[l]) / std[l];
            }
        }
    }

    return resArr;
}
